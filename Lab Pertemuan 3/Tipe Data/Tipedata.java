public class Tipedata {
    public static void main(String[] args) {
        String nama = "Belajar Java Yuks";
        System.out.println(nama.length());
        
        //Tipe data byte
        byte maxByte = 127;
        byte minByte = -128;
        byte defaultByte = 0;
        System.out.println(defaultByte);
        
        //Tipe data short
        short maxShort = 32767;
        short minShort = -32768;
        short defaultShort = 0;
        System.out.println(defaultShort);
        
        //Tipe data int
        int maxInt = 2147483647;
        int minInt = -2147483648;
        int defaultInt = 0;
        System.out.println(defaultInt);
        
        //Tipe data long
        long maxLong = 9223372036854775807L;
        long minLong = -9223372036854775808L;
        long defaultLong = 0L;
        System.out.println(defaultLong);
        
        //Tipe data float
        float maxFloat = 3.4028234799999999999999999999999999999999999999f;
        float minFloat = -3.4028234999999999999999999999999999999999999999f;
        float defaultFloat = 0.0f;
        System.out.println(maxFloat);
        System.out.println(minFloat);
        
        //Tipe data double
        double randomDouble = 1.797693134862315702d;
        double randomDouble1 = -1.797693134862315602d;
        double defaultDouble = 0.0d;
        System.out.println(randomDouble);
		
		//Tipe data boolean
		boolean menikah = false;
		System.out.println(menikah);
		
		//Tipe data char
		char A = 'A';
		System.out.println(A);
	
        //Tipe data string
        String namasaya = ("Taofik Rakhman Sudrajat");
        System.out.println(namasaya.length());
        System.out.println(namasaya);
        String alamatsaya = ("Perumahan Taman Cileunyi Blok 2F12 No 13");
        System.out.println(alamatsaya.length());
        System.out.println(alamatsaya);
    }
    
}